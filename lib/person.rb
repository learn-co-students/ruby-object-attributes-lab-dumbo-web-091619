class Person
  
  def initialized(name, job)
    @name
    @job
  end
  
  # -----------------------
  # Getters
    # - This reads their name and job
  
  def name
    @name
  end
  
  def job
    @job
  end
  
  # -----------------------
  # Setters
    # - This changes their name and their job
  
  def name=(new_name)
    @name=new_name
  end
  
  def job=(new_job)
    @job=new_job
  end
  
end